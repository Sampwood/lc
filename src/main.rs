pub fn maximal_square(matrix: Vec<Vec<char>>) -> i32 {
    let mut result = 0;
    for i in 0..matrix.len() {
        let row = &matrix[i];
        let mut len = 0;
        println!("i:{}, result:{}================", i, result);

        for j in 0..row.len() {
            println!("  j:{}", j);
            let item = row[j];
            println!("  item:{}, len:{}", item, len);
            if item == '0' {
                len = 0;
            } else if len == 0 {
                len = 1;
                result = cmp::max(result, len);
            } else {
                println!(" i:{}, j:{}, len:{}, result:{}", i, j,len, result);
                if i + len > matrix.len() - 1 {
                    len = 0;
                } else {
                    println!("    for loop len:{}======", len);
                    let mut reduce = 0;
                    for k in 1..len+1 {
                        println!("    k:{}", k);
                        println!("    {},{},{},{}", i + k,j,i + len,j - k);
                        if matrix[i + k][j] == '0' {
                            len = 0;
                            break;
                        }
                        if matrix[i + len][j - k] == '0' {
                            reduce = cmp::max(reduce, 1);
                        }
                    }
                    len = len - reduce;
                    println!("    for loop len:{}======", len);
                }
                len = len + 1;
                result = cmp::max(result, len);
            }
        }
    }

    (result * result) as i32
}